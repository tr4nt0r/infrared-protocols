    POWER = 0x08
    POWER_ON = 0xC4
    POWER_OFF = 0xC5
    RED = 0x71
    REWIND = 0x8F
    SAP = 0x0A
    SETTINGS = 0x45
    STOP = 0xB1
    SUBTITLE = 0x39
    TEXT = 0x20
    VOLUME_DOWN = 0x03
    VOLUME_UP = 0x02
    YELLOW = 0x61

    def to_command(self, repeat_count: int = 0) -> Command:
        """Build an NEC command for this LG TV code."""
        return NECCommand(
            address=LG_ADDRESS, command=self.value, repeat_count=repeat_count
        )


